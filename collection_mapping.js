// 5. Helper to build gemstone name â†’ collection ID map from your collections JSON
export function buildGemstoneCollectionMap(collections) {
    const map = {};
    for (const collection of collections) {
        if (collection.title.includes('STONE')) {
            const stoneName = collection.title.split(' ')[0];
            map[stoneName] = collection.id;
        }
    }
    return map;
}